package com.digitalgd.data.dto;

public class Error {
    private String code;
    private String text;

    public Error() {

    }

    public Error(String code, String text) {
        this.code = code;
        this.text = text;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
